    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        if (tbReparacion.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Tienes que seleccionar una fila para dar de baja una reparación");
            return;
        }
        if (tbReparacion.getSelectedRowCount() > 1) {
            JOptionPane.showMessageDialog(this, "Selecciona solo una fila");
            return;
        }
        if (txtImporte1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El importe no puede estar vacío.");
            return;
        }

        String fechaE = modeloTabla2.getValueAt(tbReparacion.getSelectedRow(), 0).toString();
        String matricula = modeloTabla2.getValueAt(tbReparacion.getSelectedRow(), 1).toString().trim();

        Timestamp fechaITS = Timestamp.valueOf(LocalDateTime.of(Integer.parseInt(spYear1.getValue().toString()), Integer.parseInt(spMes1.getValue().toString()),
                Integer.parseInt(spDia1.getValue().toString()), Integer.parseInt(spHora1.getValue().toString()), Integer.parseInt(spMin1.getValue().toString()),
                Integer.parseInt(spSeg1.getValue().toString())));
        try {
            Conexion.abrirBD();
            GestionReparaciones.bajaReparacion(fechaE, fechaITS, Double.parseDouble(txtImporte1.getText()), matricula);
            String numEmp = modeloTabla2.getValueAt(tbReparacion.getSelectedRow(), 2).toString();

            GestionReparaciones.updateImporteBonificacion(Integer.parseInt(numEmp), spMes1.getValue().toString());
            modeloTabla2.setRowCount(0);

            GestionReparaciones.listarReparacion(jList2.getSelectedValue().getCodcli(), modeloTabla2);

            JOptionPane.showMessageDialog(this, "Reparación actualizada");
            Conexion.getSession().getTransaction().commit();
        } catch (HibernateException ex) {
            Conexion.getSession().getTransaction().rollback();
        } finally {
            Conexion.cerrarBD();
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

