    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Conexion.abrirBD();
            if (comprobacionesEmpleado()) {
                GestionReparaciones.altaEmpleado(Integer.parseInt(txtCod.getText()), txtNombre.getText(), Float.parseFloat(txtSalario.getText()));
                GestionReparaciones.llenarComboEmpleados(cbEmpleados);
                Conexion.getSession().getTransaction().commit();
            } else {
                JOptionPane.showMessageDialog(this, "Faltan datos");
            }
        } catch (HibernateException ex) {

            Conexion.getSession().getTransaction().rollback();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El salario es numérico");
        } finally {
            Conexion.cerrarBD();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    boolean comprobacionesEmpleado() {
        boolean comp = true;
        if (txtCod.getText().isEmpty()) {
            comp = false;
        }
        if (txtNombre.getText().isEmpty()) {
            comp = false;
        }
        if (txtSalario.getText().isEmpty()) {
            comp = false;
        }
        return comp;
    }

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (cbEmpleados.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "No hay empleado seleccionado");
            return;
        }

        if (tbCoches.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Tienes que seleccionar una fila para dar de alta una reparación");
            return;
        }
        if (tbCoches.getSelectedRowCount() > 1) {
            JOptionPane.showMessageDialog(this, "Selecciona solo una fila");
            return;
        }
        if (txtImporte.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El importe no puede estar vacío.");
            return;
        }

        try {
            Conexion.abrirBD();
            if (GestionCoche.estaCocheEnReparacion(modeloTabla.getValueAt(tbCoches.getSelectedRow(), 0).toString())) {
                JOptionPane.showMessageDialog(this, "El coche ya se está reparando.");
                return;
            } else {

                Timestamp fechaITS = Timestamp.valueOf(LocalDateTime.of(Integer.parseInt(spYear.getValue().toString()), Integer.parseInt(spMes.getValue().toString()),
                        Integer.parseInt(spDia.getValue().toString()), Integer.parseInt(spHora.getValue().toString()), Integer.parseInt(spMin.getValue().toString()),
                        Integer.parseInt(spSeg.getValue().toString())));
                System.out.println(fechaITS);
                Empleado e = (Empleado) cbEmpleados.getSelectedItem();

                GestionReparaciones.altaReparacion(e.getCodemp(), modeloTabla.getValueAt(tbCoches.getSelectedRow(), 0).toString(), Double.parseDouble(txtImporte.getText()), fechaITS, e);
                modeloTabla2.setRowCount(0);

                GestionReparaciones.crearBonificacionMesEmpleado(e.getCodemp(), String.valueOf(fechaITS.toLocalDateTime().getMonthValue()));

                Conexion.getSession().getTransaction().commit();

            }
        } catch (HibernateException ex) {
            Conexion.getSession().getTransaction().rollback();
        } finally {
            Conexion.cerrarBD();
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

